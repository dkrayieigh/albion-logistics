function copyEntry(entry) {
  return { ...entry };
}

function isValidCategory(category) {
  return category === 'artifact' || category === 'alchemy';
}

export function listSpecialMaterialCatalogEntries() {
  return SPECIAL_MATERIAL_CATALOG.map(copyEntry);
}

export function findSpecialMaterialCatalogEntry({ category, rawName } = {}) {
  if (!isValidCategory(category) || typeof rawName !== 'string') return null;
  const trimmedRawName = rawName.trim();
  const entry = SPECIAL_MATERIAL_CATALOG.find(
    catalogEntry => catalogEntry.category === category && catalogEntry.rawName === trimmedRawName
  );
  return entry ? copyEntry(entry) : null;
}

export function findSpecialMaterialCatalogEntryByStableId(stableId) {
  if (typeof stableId !== 'string') return null;
  const trimmedStableId = stableId.trim();
  const entry = SPECIAL_MATERIAL_CATALOG.find(catalogEntry => catalogEntry.stableId === trimmedStableId);
  return entry ? copyEntry(entry) : null;
}
